            <h1>Política de privacidade</h1>
            <p class="legal-meta">Última atualização: maio de 2026</p>
            <p class="legal-lead">Esta política descreve como tratamos dados no contexto do site e da plataforma Auvvo, em linha com a LGPD (Lei nº 13.709/2018).</p>

            <h2>1. Controlador</h2>
            <p>O controlador dos dados tratados em nome da operação Auvvo é identificado no cadastro comercial e pode ser contatado em <a href="mailto:contato@Auvvo.com">contato@Auvvo.com</a>.</p>

            <h2>2. Dados que podemos coletar</h2>
            <ul class="legal-list">
                <li>Dados de cadastro: nome, e-mail, informações necessárias para cobrança quando aplicável.</li>
                <li>Dados de uso: logs técnicos, métricas de painel e interações necessárias para prestação do serviço.</li>
                <li>Conteúdo operacional: textos e materiais que você insere para treinar agentes e fluxos de atendimento.</li>
            </ul>

            <h2>3. Finalidades</h2>
            <p>Prestação do serviço, suporte, segurança, cumprimento legal, melhoria do produto e comunicações relacionadas à conta.</p>

            <h2>4. Compartilhamento</h2>
            <p>Podemos utilizar provedores de infraestrutura, pagamento e APIs de IA (ex.: OpenAI) conforme necessário à operação. Esses parceiros tratam dados segundo seus próprios termos e conforme contratos aplicáveis.</p>

            <h2>5. Direitos do titular</h2>
            <p>Você pode solicitar confirmação de tratamento, acesso, correção, anonimização, portabilidade ou eliminação de dados pessoais, nos limites legais, pelo e-mail acima.</p>

            <h2>6. Retenção</h2>
            <p>Mantemos dados pelo tempo necessário à prestação do serviço e obrigações legais; prazos específicos podem variar por tipo de dado.</p>

